public class SumOfNumbers {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;
        int num3 = 9;
        int sum = num1 + num2 + num3;
        System.out.println("Sum of given numbers:- " + num1 + ", " + num2 + ", " + num3 + " is: ");
        System.out.println(sum);
    }
}
